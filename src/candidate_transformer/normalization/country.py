def normalize_country_to_alpha2(value: str | None) -> str | None:
    """Normalize a country value to ISO-3166 alpha-2."""
    if value is None:
        return None

    text = str(value).strip()
    if not text:
        return None

    upper = text.upper()
    if re.fullmatch(r"[A-Z]{2}", upper) and upper in _ISO_ALPHA2_CODES:
        return upper

    alias_key = re.sub(r"\s+", " ", text.casefold().replace(".", "")).strip()
    return _COUNTRY_ALIASES.get(alias_key)


def is_valid_alpha2_country(value: str) -> bool:
    """Return whether a value is a valid ISO-3166 alpha-2 country code."""
    return value.upper() in _ISO_ALPHA2_CODES
